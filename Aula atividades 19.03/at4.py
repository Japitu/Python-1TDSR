n1 = int(input("Informe um número: "))
n2 = int(input("Informe outro número: "))


soma = n1 + n2
prod = n1 * n2
divIn = n1 // n2
rest = n1 % n2 

print("Soma dos 2 números: ")
print(f"{n1} + {n2} = {soma}")
print("Multiplicação dos 2 números: ")
print(f"{n1} * {n2} = {prod}")
print("Divisão Inteira dos 2 números: ")
print(f"{n1} // {n2} = {divIn}")
print("Resto da divisão dos 2 números: ")
print(f"{n1} % {n2} = {rest}")